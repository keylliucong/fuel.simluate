	!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!TDMA算法!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!
	!!!!!!!!!!!!!!形参说明：                                                     !!!!!!!!!!!!!!
	!!!!!!!!!!!!!!          A―――――双精度二维数组,体积为N*N,输入参数.方程组的!!!!!!!!!!!!!!
	!!!!!!!!!!!!!!          系数矩阵,要求主对角线占绝对优势.                     !!!!!!!!!!!!!!
	!!!!!!!!!!!!!!          B―――――双精度实型一维数组,长度为N,输入参数.方程组!!!!!!!!!!!!!!
