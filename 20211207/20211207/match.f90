	!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!G-S迭代算法!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!
	!!!!!!!!!!!!!!形参说明：                                                     !!!!!!!!!!!!!!
	!!!!!!!!!!!!!!          A―――――双精度二维数组,体积为N*N,输入参数.方程组的!!!!!!!!!!!!!!
	!!!!!!!!!!!!!!          系数矩阵,要求主对角线占绝对优势.                     !!!!!!!!!!!!!!
	!!!!!!!!!!!!!!          B―――――双精度实型一维数组,长度为N,输入参数.方程组!!!!!!!!!!!!!!
	!!!!!!!!!!!!!!          右端的常数向量.                                      !!!!!!!!!!!!!!
	!!!!!!!!!!!!!!          N―――――整型变量,输入参数.方程组阶数.             !!!!!!!!!!!!!!
	!!!!!!!!!!!!!!          X―――――双精度实型一维数组,长度为N,输出参数.返回方!!!!!!!!!!!!!!
	!!!!!!!!!!!!!!          程组的解向量.                                        !!!!!!!!!!!!!!
	!!!!!!!!!!!!!!          EPS――――实型变量,输入参数,控制精度要求.           !!!!!!!!!!!!!!
	!!!!!!!!!!!!!!          L―――――整型变量,输出参数.若返回L=0,说明子程序工作!!!!!!!!!!!!!!
	!!!!!!!!!!!!!!          失败(如系数矩阵奇异,迭代不收敛)；若L/=0，表示正常返回!!!!!!!!!!!!!!
	!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!
	subroutine AGSDL(A,B,N,X,EPS,L)
	integer N
	dimension A(N,N),B(N),X(N)
	double precision A,B,X,T,S,P,Q
	do 5 I=1,N
	  if (abs(A(I,I))+1.0.EQ.1.0) then
		L=0
		write(*,100)
		return
	  end if
5	continue
100	format(1X,'  FAIL')
	L=100
	do 10 I=1,N
10  X(I)=0.0
20	P=0.0
	L=L-1
	do 50 I=1,N
	  T=X(I)
	  S=0.0
	  do 30 J=1,N
		if (J.NE.I) S=S+A(I,J)*X(J)
30	  continue
	  X(I)=(B(I)-S)/A(I,I)
	  Q=abs(X(I)-T)/(1+abs(X(I)))
	  if (Q.GT.P) P=Q
50	continue
	if ((P.GE.EPS).AND.(L.NE.0)) goto 20
	if (L.EQ.0) write(*,100)
	return
	end subroutine
